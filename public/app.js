// Grab the articles as a json
function scrapeArticles() {
  $.getJSON("/articles", function (data) {
    // For each one
    for (var i = 0; i < data.length; i++) {
      // Display the apropos information on the page
      //$("#articles").append("<p data-id='" + data[i]._id + "'>" + data[i].image + " " + data[i].title + "<br />" + data[i].link + "</p>");

      $("#articles").append(createCard(data[i]))
    }
  });
}

function createCard(article) {
  var card = $("<div class='card'>");
  var cardHeader = $("<div class='card-header'>").append(
    $("<h3>").append(
      $("<a class='article-link' target='_blank' rel='noopener noreferrer'>")
        .attr("href", article.link)
        .text(article.title),
      $("<a class='btn btn-success save'>Save Article</a>")
    )
  );

  //var cardBody = $("<div class='card-body'>").text(article.summary);
  var cardBody = $("<div class='card-body'>").html("<p data-id='" + article._id + "'>" + article.title + "</p>")
  var cardImage = `<img class='card-img-top responsive' src=${article.image}>`
  card.append(cardImage, cardHeader, cardBody);
  // We attach the article's id to the jQuery element
  // We will use this when trying to figure out which article the user wants to save
  card.data("_id", article._id);
  // We return the constructed card jQuery element
  return card;

}

// Whenever someone clicks a p tag
$(document).on("click", "p", function () {
  // Empty the notes from the note section
  $("#notes").empty();
  // Save the id from the p tag
  var thisId = $(this).attr("data-id");

  // Now make an ajax call for the Article
  $.ajax({
    method: "GET",
    url: "/articles/" + thisId
  })
    // With that done, add the note information to the page
    .then(function (data) {
      console.log(data);
      // The title of the article
      $("#notes").append("<h2>" + data.title + "</h2>");
      // An input to enter a new title
      $("#notes").append("<input id='titleinput' name='title' >");
      // A textarea to add a new note body
      $("#notes").append("<textarea id='bodyinput' name='body'></textarea>");
      // A button to submit a new note, with the id of the article saved to it
      $("#notes").append("<button data-id='" + data._id + "' id='savenote'>Save Note</button>");

      // If there's a note in the article
      if (data.note) {
        // Place the title of the note in the title input
        $("#titleinput").val(data.note.title);
        // Place the body of the note in the body textarea
        $("#bodyinput").val(data.note.body);
      }
    });
});

// When you click the savenote button
$(document).on("click", "#savenote", function () {
  // Grab the id associated with the article from the submit button
  var thisId = $(this).attr("data-id");

  // Run a POST request to change the note, using what's entered in the inputs
  $.ajax({
    method: "POST",
    url: "/articles/" + thisId,
    data: {
      // Value taken from title input
      title: $("#titleinput").val(),
      // Value taken from note textarea
      body: $("#bodyinput").val()
    }
  })
    // With that done
    .then(function (data) {
      // Log the response
      console.log(data);
      // Empty the notes section
      $("#notes").empty();
    });

  // Also, remove the values entered in the input and textarea for note entry
  $("#titleinput").val("");
  $("#bodyinput").val("");
});
